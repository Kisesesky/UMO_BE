  // 시도 이름으로 regId를 찾는 헬퍼 함수
  export function findRegIdBySido(sido: string, gugun?: string): string | null {
    for (const mapping of REGION_MAPPINGS) {
      for (const region of mapping.regions) {
        if (region.sido === sido) {
          // 구군이 지정된 경우, 해당 구군이 있는지 확인
          if (gugun && region.guguns) {
            if (region.guguns.includes(gugun)) {
              return mapping.regId;
            }
          } else {
            // 구군이 지정되지 않은 경우, 시도만으로 매핑
            return mapping.regId;
          }
        }
      }
    }
    return null; // 매핑을 찾지 못한 경우
  }